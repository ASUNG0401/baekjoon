import java.util.Scanner;
public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in); //sc라는 스캐너 변수 만들기

        String st = sc.next(); // st라는 입력값 변수 만들기
        System.out.println((st.length())); // st.length로 길이 측정
        sc.close();
//-------------------------------------------------------------------
    }
}
    

